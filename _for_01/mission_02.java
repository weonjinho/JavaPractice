package _for_01;

public class mission_02 {

	public static void main(String[] args) {
//		2.
		for(int i=5; 5>=0; i--){
			System.out.println(i);
		}
		
//		<문제 분석>
//		초기값: 5
//		조건식: 5가0보다 크거나 작을때. (조건이 참이면, 코드불록이 실행되고, 거짓이면 실행이 안된다.)
//		증감식: 코드블록 실행 후, i를 1씩 감소. 
		
//		이 문제 조건식은 항상 참이며,이 반복문을 무할루프를 돌것이다.
		
		
	}

}
